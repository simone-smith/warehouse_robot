require 'pry'

class Warehouse

  def initialize
    @board = [
      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["X","-","-","-","-","-","-","-","-","-"]
    ]
    @x = 0
    @y = 9
  end

  def display
    @board
  end

  def move_robot(direction)
    if direction == "N"
      @board[@y][@x] = "-"
      @y -= 1
      @board[@y][@x] = "X"
    elsif direction == "E"
      @board[9][0] = "-"
      @board[9][1] = "X"
    end
  end
end
