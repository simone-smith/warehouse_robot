      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"],
      ["X","-","-","-","-","-","-","-","-","-"],
      ["-","-","-","-","-","-","-","-","-","-"]
    ]
  end
end
